function ecoq_normalize_news_slug(string $value): string
{
    $value = trim(strtolower($value));
    $value = preg_replace('/[^a-z0-9\-]+/', '-', $value) ?? $value;
    $value = preg_replace('/-+/', '-', $value) ?? $value;
    return trim($value, '-');
}

function ecoq_find_news_by_slug(string $slug): ?array
{
    $items = ecoq_news_items();
    $normalizedTarget = ecoq_normalize_news_slug($slug);

    foreach ($items as $index => $item) {
        $candidates = array_merge(
            [$item['slug'] ?? ''],
            $item['aliases'] ?? []
        );

        foreach ($candidates as $candidate) {
            if ($candidate === $slug || ecoq_normalize_news_slug((string) $candidate) === $normalizedTarget) {
                $item['_index'] = $index;
                return $item;
            }
        }
    }

    return null;
}
